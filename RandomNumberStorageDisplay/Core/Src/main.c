void Read(uint16_t MemLoc, uint8_t *pData, uint16_t len)
{
	uint8_t addr[2];
	addr[0] = (uint8_t)((MemLoc & 0xFF00) >> 8);
	addr[1] = (uint8_t)(MemLoc & 0x00FF);
	HAL_I2C_Master_Transmit(&hi2c1,I2C_ADDRESS, addr, 2, HAL_MAX_DELAY);
	HAL_I2C_Master_Receive(&hi2c1,I2C_ADDRESS, pData, len, HAL_MAX_DELAY);
}

void Write(uint16_t MemLoc, uint8_t *pData, uint16_t len)
{
	uint8_t data[len+2];
	data[0] = (uint8_t)((MemLoc & 0xFF00) >> 8);
	data[1] = (uint8_t)(MemLoc & 0x00FF);
	memcpy(&data[2], pData, len);

	HAL_I2C_Master_Transmit(&hi2c1,I2C_ADDRESS, data, len+2, HAL_MAX_DELAY);

    while(HAL_I2C_Master_Transmit(&hi2c1,I2C_ADDRESS, NULL, 0, HAL_MAX_DELAY) != HAL_OK);
}

uint16_t Generate_Random_Number(RTC_HandleTypeDef *hrtc, ADC_HandleTypeDef *hadc)
{
	RTC_TimeTypeDef sTime;
	RTC_DateTypeDef sDate;
	// Get RTC time and date
	HAL_RTC_GetTime(hrtc, &sTime, RTC_FORMAT_BIN);
	HAL_RTC_GetDate(hrtc, &sDate, RTC_FORMAT_BIN);

	// Get ADC value
	HAL_ADC_Start(hadc);
	HAL_ADC_PollForConversion(hadc, 10);
	uint32_t adc_value = HAL_ADC_GetValue(hadc);

	uint32_t seed = (sTime.Seconds + sTime.Minutes * 60 + sTime.Hours * 3600 + sDate.Date * 86400 + sDate.Month * 2592000 + sDate.Year * 31104000) ^ adc_value;  // Generate random seed (RTC ^ ADC)
	srand(seed);

	uint16_t random_number = rand() % 10000;  // Generate random number 0-9999

    return random_number;
}

